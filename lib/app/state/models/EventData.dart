class EventData {
  final String title;
  final String description;
  final String location;
  final DateTime date;
  final String region;
  final String city;
  final String prize;
  final String conditions;

  EventData({
    required this.title,
    required this.description,
    required this.date,
    required this.location,
    required this.region,
    required this.city,
    required this.prize,
    required this.conditions,
  });
}
